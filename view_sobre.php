<p class="section-description mt-2 pt-4" id="des"><strong>Objetivos pelos quais desencandeou o nosso projeto:</strong><br></br>
<strong>A importância dos pontos hoje em dia</strong><br>
Não há importância expressiva e significativa em relação ao turismo; muito mais gastos são
aplicados em outras zonas da sociedade, como por exemplo: ambientes de lazer. Assim sendo, os
pontos turíticos são desprezados em matéria de custos mesmo contendo responsabilidades socias
que engrandecem a cidade.<br>
<strong> O aumento da economia com os pontos</strong><br>
Os pontos turísticos por serem bastante requisitados elevam a economia visto que há um rendimento
e desenvolvimento maior do comércio por receberem estrangeiros que inserem dinheiro em grande
escala com compras e aluguéis.<br>
<strong> Dificuldade ao procurar pontos</strong><br>  
Há uma dificuldade no que tange às procuras relativas a pontos turísticos; indivíduos tentam
insitentemente buscar lugares apreciosos, todavia não é fácil encontrar o que requer considerando o
investimento baixo em pontos turísticos.<br>
<strong>Buscas mais rápidas e objetivas</strong><br>
As pesquisas por pontos turísticos com um auxílio de um site altera relativamente sobremodo os
peíodos de procura e a objetividade dessas procuras no tocante ao que realmente é desejado.

</br></br>


